#Printing a perfect square
for i in range(4):
    for w in range(4):
        print('# ',end='')
    print()
print()
#Printing Triangle
for i in range(4):
    for w in range(i+1):#we are only going till 'i+1'
        print('# ',end='')
    print()
print()
#Printing a triangle upside down
for i in range(4):
    for w in range(4-i):#we are going for '4-i'
        print('# ',end='')
    print()